# Description: Takes in a float temperature in Celsius and converts to its corresponding temperature
#              in Fahrenheit

print("Please enter the temperature in Celsius.")

temp_c = float(input())
temp_f = (9/5) * temp_c + 32

print("The corresponding temperature in Fahrenheit is:")
print(temp_f)

